package quiz;

public class B09_printAlpahbet {
	public static void main(String[] args) {
		//반복문을 이용하여 A부터 Z까지 출력하라
		for(int i=65; i<=90; i++) {
			System.out.print((char)i);
		}
		
		System.out.println();
		
		//반복문을 사용하여 z부터 a 까지 출력하세요
		for(int i= 122; i>=97; i--) {
			System.out.print((char)i);
		}
		
	}
}
